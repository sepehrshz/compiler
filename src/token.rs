#[derive(Debug ,PartialEq, Eq)]
pub enum TokenType {
    Identifer,
    BOOL,
    BREAK,
    CHAR,
    CONTINUE,
    ELSE,
    FALSE,
    FOR,
    IF,
    ASSIGN,
    INT,
    PRINT,
    RETURN,
    TRUE,
    OpenBracket,
    CloseBracket,
    OpenParenthesis,
    CloseParenthesis,
    OpenBraces,
    CloseBraces,
    COMMA,
    SEMICOLON,
    NUMBERDECIMAL,
    NUMBERHEX,
    NUMBERBI,
    COMMENTS,
    CHARCTER,
    STRING,
    PLUS,
    MINUS,
    MUTPLY,
    DIVIDE,
    MOD,
    EQUAIL,
    BIGGER,
    SMALLER,
    BIGGERANDEQUAIL,
    SMALLERANDEQUAIL,
    NOTEQUIL,
    AND,
    OR,
    AndLogical,
    OrLogical,
End,
    NOT,
    ILLEGAL,
}

#[derive(Debug)]
pub struct Token {
    pub token: TokenType,
    pub line: usize,
    pub literal: String,
    pub column: usize,
}
